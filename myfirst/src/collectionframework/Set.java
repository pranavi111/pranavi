package collectionframework;

import java.util.HashSet;

public class Set {

	public static void main(String[] args) {
		String data="ABCGGAAABCCCCCBBVDGGGTTHHH";	

		HashSet chars=new HashSet();

		char[] arr=data.toCharArray();

		for (char c : arr) {

			chars.add(c);		


		}

		System.out.println(chars);


	}

}
